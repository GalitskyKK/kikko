/** Returns recently used emoji chars (from localStorage), then rest of list. */
export function getEmojiWithFrequentFirst(
  list: EmojiEntry[],
  recentMax = 24,
): EmojiEntry[] {
  if (typeof window === 'undefined') return list
  try {
    const raw = window.localStorage.getItem(EMOJI_FREQUENT_KEY)
    const recent: string[] = raw ? (JSON.parse(raw) as string[]) : []
    const recentSet = new Set(recent.slice(0, recentMax))
    const frequent: EmojiEntry[] = []
    const rest: EmojiEntry[] = []
    for (const entry of list) {
      if (recentSet.has(entry.emoji)) {
        frequent.push(entry)
      } else {
        rest.push(entry)
      }
    }
    const order = recent.slice(0, recentMax)
    const orderedFrequent = order
      .map((char) => frequent.find((e) => e.emoji === char))
      .filter((e): e is EmojiEntry => Boolean(e))
    return [...orderedFrequent, ...rest.filter((e) => !recentSet.has(e.emoji))]
  } catch {
    return list
  }
}

/** Push emoji to "recently used" in localStorage. */
export function markEmojiUsed(emojiChar: string): void {
  if (typeof window === 'undefined') return
  try {
    const raw = window.localStorage.getItem(EMOJI_FREQUENT_KEY)
    const recent: string[] = raw ? (JSON.parse(raw) as string[]) : []
    const next = [emojiChar, ...recent.filter((c) => c !== emojiChar)].slice(0, 32)
    window.localStorage.setItem(EMOJI_FREQUENT_KEY, JSON.stringify(next))
  } catch {
    // no-op
  }
}

/** Filter emoji list by query: name and slug (underscore-separated) match. */
export function filterEmojiList(list: EmojiEntry[], query: string): EmojiEntry[] {
  const normalized = query.trim().toLowerCase()
  if (!normalized) return list
  const tokens = normalized.split(/\s+/)
  return list.filter((entry) => {
    const searchable = `${entry.name} ${entry.name.replace(/\s+/g, '_')}`.toLowerCase()
    return tokens.every((t) => searchable.includes(t))
  })
}

export interface EmojiGridSection {
  title: string
  count: number
  entries: EmojiEntry[]
}

/** Build sections for grid: "Frequently Used" (from localStorage) then by group. */
export function getEmojiGridSections(
  list: EmojiEntry[],
  recentMax = 32,
): EmojiGridSection[] {
  if (list.length === 0) return []
  if (typeof window === 'undefined') {
    const byGroup = new Map<string, EmojiEntry[]>()
    for (const e of list) {
      const arr = byGroup.get(e.group) ?? []
      arr.push(e)
      byGroup.set(e.group, arr)
    }
    return Array.from(byGroup.entries(), ([title, entries]) => ({
      title,
      count: entries.length,
      entries,
    }))
  }
  try {
    const raw = window.localStorage.getItem(EMOJI_FREQUENT_KEY)
    const recent: string[] = raw ? (JSON.parse(raw) as string[]) : []
    const recentOrder = recent.slice(0, recentMax)
    const recentSet = new Set(recentOrder)
    const frequent: EmojiEntry[] = recentOrder
      .map((char) => list.find((e) => e.emoji === char))
      .filter((e): e is EmojiEntry => Boolean(e))
    const rest = list.filter((e) => !recentSet.has(e.emoji))
    const byGroup = new Map<string, EmojiEntry[]>()
    for (const e of rest) {
      const arr = byGroup.get(e.group) ?? []
      arr.push(e)
      byGroup.set(e.group, arr)
    }
    const sections: EmojiGridSection[] = []
    if (frequent.length > 0) {
      sections.push({ title: 'Frequently Used', count: frequent.length, entries: frequent })
    }
    for (const [title, entries] of byGroup.entries()) {
      sections.push({ title, count: entries.length, entries })
    }
    return sections
  } catch {
    const byGroup = new Map<string, EmojiEntry[]>()
    for (const e of list) {
      const arr = byGroup.get(e.group) ?? []
      arr.push(e)
      byGroup.set(e.group, arr)
    }
    return Array.from(byGroup.entries(), ([title, entries]) => ({
      title,
      count: entries.length,
      entries,
    }))
  }
}
